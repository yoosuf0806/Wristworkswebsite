// Renders one or more JSON-LD objects into <script type="application/ld+json">.
// Server component — the structured data ships in the initial HTML for crawlers.
export function JsonLd({ data }: { data: Record<string, unknown> | Record<string, unknown>[] }) {
  const items = Array.isArray(data) ? data : [data];
  return (
    <>
      {items.map((item, i) => (
        <script
          key={i}
          type="application/ld+json"
          // JSON is serialised safely; angle brackets escaped to avoid breaking out.
          dangerouslySetInnerHTML={{
            __html: JSON.stringify(item).replace(/</g, "\\u003c"),
          }}
        />
      ))}
    </>
  );
}
